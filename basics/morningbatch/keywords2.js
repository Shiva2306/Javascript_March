

//Re initialization
//this is a important feature of const keyword
//in playwright, whenever we want to fix the variable value, we will use const keyword

var a=10; // initialization
   a=20;  //re initialization
   console.log(a); 

let b=10; // initialization
   b='kumar';  //re initialization
   console.log(b); 

const c=10; // initialization
      c=20; //re initialization
      console.log(c);

//const will not allow us to re initilize the variable
//because if we use const keyword, that means that variable is fixed and we cannot do any modification to that variable